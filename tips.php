<p>Landing the job of your dreams… What does that look like to you? It might be to produce really cool videos for a brand you love, designing posters for new products or analyzing data. Either way, before actually getting the job, the most important part is getting an interview and rocking it!Before a recruiter calls you to say “Heyyyyyy…”, you need to grab their attention with your resume. In a highly competitive job search, setting yourself apart from the masses starts here. Your resume is your opportunity to make a great first impression! Follow these resume tips and write a resume you’ll feel proud of sharing with potential employers.</p>
<h2>1. It’s not what you say on your resume, but how you say it</h2>
<p>Believe it or not, the context of your resume makes a dramatic difference in the way a would-be employer sees you as a prospective employee. Writing in a formal yet personable tone shows the employer that you’re not a robot, and in addition to being professional, you have personality. Instead of providing bland sentences and pre-selected job descriptions, tweak the sentences to give them spice. For example, try writing “I have gained knowledge and expertise working at the zoo, and I hope that I will be able to use my skills in my new profession,” instead of just writing: “I worked at a zoo”. See what we did there?</p>
<h2>2. A little embellishment never hurt anybody</h2>
<p><img style="width: 128px; height: 128px; float: right;" src="./ResumeTipsImg/resume-tips(1).png" alt="Resume Tips" /><br />
Think of your resume as an advertisement selling your service. You need to make sure it lures the potential employer into hiring you without being overly wordy. That being said, it’s highly important to add resume keywords that appeal to the recruiter. However, you should never lie on your resume. Making stuff up on your resume will only put you in a very tight spot once you’re called in for an interview. Use the right resume keywords, present your work history in an appealing way but don’t overdo it. Would you add rhinestones to an entire dress? Nope. Rhinestones should be used sparingly, or at least that’s what we heard somewhere, and the same rule applies to keywords on your resume.</p>
<h2>3. Proofreading says you care</h2>
<p>Show prospective employers that you’re reliable, not careless. Grammar mistakes on your resume send your employer the wrong message, because it kind of says: “I’m not interested in polishing my resume for you, so I’m not that interested in getting the job.” Avoid giving potential employers a reason to think you’re unfit for the job. Yes, we get it. Proofreading is tedious and boring, but it should never be skipped! There are a lot of mistakes hiding in your resume’s corner, and proofreading your resume ensures that it will be mistake-free. Spell check software doesn’t solve anything because it can’t recognize common errors in your work. So, use your awesome human powers to make sure you send out a perfect resume.</p>
<p><img style="width: 128px; height: 128px; float: left;" src="./ResumeTipsImg/proofreading(1).png" alt="Proofreading" /></p>
<p>Most common mistakes are from homonym confusion, and these mistakes can change the entire meaning of your resume. If you want to proofread effectively, wait a day after you write your resume to proofread it, because proofreading familiar work increases your chances of sending it with grammatical errors. Among the key resume tips for proofreading is to ask a friend or family member to review your resume. They can catch errors you may overlook. If you can’t get somebody to read it for you, read your resume out loud to help you spot awkward phrases, misspellings, homonym issues and other common grammar errors.</p>
<h2>4. Break up with long paragraphs</h2>
<p>Make sure recruiters can read your resume easily. If you choose a really tiny, hard-to-see font, then your prospective employer will have a difficult time reading the resume and will probably miss out on important information. Another common problem is not providing paragraph breaks. Which would you rather read: a book with easy–to-read fonts and paragraph breaks or a book with unreadable fonts and no paragraph breaks? To ensure recruiters read the relevant parts of your resume, be mindful of your resume format.</p>
<h2>5. Font size does matters</h2>
<p>Choosing the right font is crucial because it makes a resume easy–to-read and professional. Instead of using flashy or colorful fonts that blind your recruiter, try to stick with classic ones like Arial. The size of the font is highly important because you don’t want it to be too big, nor do you want it to be too small. The ideal font size is 12 because it’s large enough to read clearly, but it doesn’t look overwhelming and like you’re expecting a 90-year-old recruiter to interview you.</p>
<h2>6. Achievements unlocked</h2>
<p>Employers are looking to hire people who have the necessary credential and experiences. Are you the professional they need? One of the escential resume tips is to highlight every achievement you have accomplished during your career if you want to receive an interview invitation. Always describe your academic accomplishments, your work experiences, your talents and your skills tailored to the job description you’re applying for.</p>
<p>Overall, when writing your resume, you should show the employer the qualities that make you fit for that specific job title. You need to make yourself look like the best job candidate, and if you do that successfully, you’ll be able to land the job of your dreams.</p>
